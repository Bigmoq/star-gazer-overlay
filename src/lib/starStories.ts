function hashString(str: string): number {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    hash = ((hash << 5) - hash + str.charCodeAt(i)) | 0;
  }
  return Math.abs(hash);
}

export function getStarStory(nameOrId: string): string {
  const index = hashString(nameOrId) % starStories.length;
  return starStories[index];
}
